# Example 1
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is less than or equal to 5")

# Example 2
y = 7
if y % 2 == 0:
    print("y is even")
else:
    print("y is odd")

# Example 3
z = -3
if z < 0:
    print("z is negative")
elif z == 0:
    print("z is zero")
else:
    print("z is positive")

# Example 4
a = 15
if a > 10:
    print("a is greater than 10")
elif a == 10:
    print("a is equal to 10")
else:
    print("a is less than 10")

