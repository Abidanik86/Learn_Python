

# All data types in Python

a = 1
# display a:
print(a)
# display the data type of a:
print("a is :", type(a))

b = "Abid"
# display b:
print(b)
# display the data type of b:
print("b is :", type(b))

c = 4.02
# display c:
print(c)
# display the data type of c:
print("c is :", type(c))

d = complex(1j)
# display d:
print(d)
# display the data type of d:
print(type(d))

e = list(("apple", "banana", "cherry"))
# display e:
print(e)
# display the data type of e:
print(type(e))

f = tuple(("apple", "banana", "cherry"))
# display f:
print(f)
# display the data type of f:
print(type(f))

g = range(6)
# display g:
print(g)
# display the data type of g:
print(type(g))

h = dict(name="Abid Hasan Anik", age=26)
# display h:
print(h)
# display the data type of h:
print(type(h))

i = set(("apple", "banana", "cherry"))
# display i:
print(i)
# display the data type of i:
print(type(i))

j = frozenset(("apple", "banana", "cherry"))
# display j:
print(j)
# display the data type of j:
print(type(j))

k = bool(5)
# display k:
print(k)
# display the data type of k:
print(type(k))

l = bytes(5)
# display l:
print(l)
# display the data type of l:
print(type(l))

m = bytearray(5)
# display m:
print(m)
# display the data type of m:
print(type(m))

n = bytearray(5)
# display n:
print(n)
# display the data type of n:
print(type(n))

o = memoryview(bytes(5))
# display o:
print(o)
# display the data type of o:
print(type(o))

